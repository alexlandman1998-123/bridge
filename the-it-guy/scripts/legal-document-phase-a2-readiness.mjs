function runJson(script, timeout = 60_000) {
  const result = spawnSync(process.execPath, [script], {
    cwd: process.cwd(),
    env: process.env,
    encoding: 'utf8',
    maxBuffer: 10 * 1024 * 1024,
    timeout,
  })
  try {
    return { exitCode: result.status, report: JSON.parse(result.stdout), error: result.stderr || null }
  } catch {
    return { exitCode: result.status, report: null, error: result.stderr || result.stdout || 'No JSON report returned.' }
  }
}

